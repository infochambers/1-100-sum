// The cumulative sum, one through one hundred

// Declare where the total count goes 
var sum = 0;

// Declare what does the counting
var x = 0;

//Do the loop to count up to one hundred
while (x <= 100) {
  
  // Add the latest addition to the current count
  sum = sum + x
  
  // increment the count by one
  x++
  }
  
  //report the final number result
  console.log(sum)
